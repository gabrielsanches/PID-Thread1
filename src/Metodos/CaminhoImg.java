package Metodos;

/**
 *
 * @author Gabriel
 */
public class CaminhoImg {
    String caminho;
    String nome;

    public CaminhoImg(String caminho, String name) {
        this.caminho = caminho;
        nome=name;
    }

    public String getCaminho() {
        return caminho;
    }

    public void setCaminho(String caminho) {
        this.caminho = caminho;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }
    
}
